##append()

#append() 函式可以將項目添加在一個串列的尾端，
#如果添加的是一個序列 tuple，會原封不動的變成一個新元素 ( 只佔有一個 offset，變成第二層序列或 tuple )。

a = ['a','b','c','d','e']
a.append(100)
b = ['a','b','c','d','e']
b.append([100,200,300])
print(a)   # ['a', 'b', 'c', 'd', 'e', 100]
print(b)   # ['a', 'b', 'c', 'd', 'e', [100, 200, 300]]